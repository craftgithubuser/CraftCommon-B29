package chapter2;

public class AssignmentOperators {

	public static void main(String[] args) {
		
		int x = 100;
		
		x += 10;
		System.out.println(x);
		
		x -= 20;
		System.out.println(x);
		
		x *= 10;
		System.out.println(x);
		
		x /= 10;
		System.out.println(x);
		
		x %= 10;
		System.out.println(x);
		
		
		
		
		

	}

}
